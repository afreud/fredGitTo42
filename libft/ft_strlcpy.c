#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t		i;

	i = 0;
	if (size > 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dst[i] = src[i];
			i++;
		}
	}
	if (size > 0 && dst[i] != '\0' && i < (size))
		dst[i] = '\0';
	return (ft_strlen((char *)src));
}
